/**
 * `NotificationType` is an enumeration that represents the type of notifications.
 * It has three types of notifications:
 * - `LEAVE_BALANCE_REMINDER`: A reminder for leave balance.
 * - `MONTHLY_PAYSLIP`: A notification for the monthly payslip.
 * - `HAPPY_BIRTHDAY`: A birthday greeting notification.
 */
export enum NotificationType {
  LEAVE_BALANCE_REMINDER = 'leave-balance-reminder',
  MONTHLY_PAYSLIP = 'monthly-payslip',
  HAPPY_BIRTHDAY = 'happy-birthday',
}